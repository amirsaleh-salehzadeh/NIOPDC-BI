package aip.util.test;

public class Person {
	String name;
	String family;
	int age;
	public Person(){
		name="ali";
		family="alizadeh";
		age=18;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFamily() {
		return family;
	}
	public void setFamily(String family) {
		this.family = family;
	}
}